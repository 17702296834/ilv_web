IlvTab.change = function(num){
    for(var id=1; id<=10; id++){
        var div_title = document.getElementById("div_tab_menu"+id);
        var div_content = document.getElementById("div_tab_content"+id);
        if(div_title.className=="tab_menu_hide"){
            break;
        }
        if(id==num){
            div_content.style.display = "block";
            div_title.className = "tab_menu_on";
        } else {
            div_content.style.display = "none";
            div_title.className = "tab_menu_off";
        }
    }
}




